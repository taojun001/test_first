<p>第二条 用户身份保证<br />
  2-1 用户承诺并保证自己是具有完全民事行为能力和完全民事权利能力的自然人、法人、实体或组织。用户在此保证所填写的用户信息是真实、准确、完整、及时的，并且没有任何引人误解或者虚假的陈述，且保证香芋网可以通过用户所填写的联系方式与用户取得联系。<br />

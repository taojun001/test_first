  2-3 用户承诺将及时更新其用户信息以维持该等信息的有效性。<br />
  2-4 如果用户提供的资料或信息包含有不正确、不真实的信息，香芋网保留取消用户会员资格并随时结束为该用户提供服务的权利。<br />
